package br.com.xyinc.domain;

public class Pontos {
	
	private Long idPonto;
	private String nome_ponto;
	private String co_X;
	private String co_Y;
	public Long getIdPonto() {
		return idPonto;
	}
	public void setIdPonto(Long idPonto) {
		this.idPonto = idPonto;
	}
	public String getNome_ponto() {
		return nome_ponto;
	}
	public void setNome_ponto(String nome_ponto) {
		this.nome_ponto = nome_ponto;
	}
	public String getCo_X() {
		return co_X;
	}
	public void setCo_X(String co_X) {
		this.co_X = co_X;
	}
	public String getCo_Y() {
		return co_Y;
	}
	public void setCo_Y(String co_Y) {
		this.co_Y = co_Y;
	}
	
	

}
